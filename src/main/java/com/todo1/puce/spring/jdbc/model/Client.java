package com.todo1.puce.spring.jdbc.model;

/**
 * @author rparedes
 *
 */
public class Client {
	
	private String id;
	private String name;
	private String lastname;
	private String address;
	private String phone;
	private String idInsurance;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the lastname
	 */
	public String getLastname() {
		return lastname;
	}
	/**
	 * @param lastname the lastname to set
	 */
	public void setLastname(String lastname) {
		this.lastname = lastname;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the idInsurance
	 */
	public String getIdInsurance() {
		return idInsurance;
	}
	/**
	 * @param idInsurance the idInsurance to set
	 */
	public void setIdInsurance(String idInsurance) {
		this.idInsurance = idInsurance;
	}
	

}
